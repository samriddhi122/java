public class convertDataType {
    public static void main(String[] args) {
        double a=12.5;
        int d=(int)a;
        System.out.println("convert double to int: "+d);
        long s=(long)a;
        System.out.println("convert double to long: "+s);
        char c='A';
        float f=(float)c;
        System.out.println("convert char to float: "+f);
        int g=(int)c;
        System.out.println("convert char to int: "+g);
    }
}
